Как рассчитать скидку на товар, формула.
Если покупатель осведомлен только о размере базовой, начальной цены, зная величину скидки, можно рассчитать и сумму скидки, и новую стоимость товара.
Чаще всего скидка указывается в процентном соотношении. Как правильно рассчитать скидку на товар? Формула применяется следующая:
СС = БЦ / 100% * ПС, где
СС - сумма скидки,
БЦ - базовая цена,
ПС - % скидки.
Чтобы узнать новую цену, из базовой стоимости вычитают полученную сумму скидки.

Приведем пример расчета.
Пример расчета скидки исходя из установленного процента
ООО «Урожай» продавало яблоки с начала года по цене 40 рублей за килограмм.
В августе, в связи с сезонным снижением спроса на товар, общество объявило о предоставлении покупателям скидки в 15%.
Как правильно рассчитать скидку на товар?
40 рублей – это базовая цена,
15% - процент скидки.
Сумма снижения цены составит: 40 / 100% * 15% = 6 рублей.
Цена на яблоки в августе с учетом предоставленной скидки составит: 40 – 6 = 34 рубля за килограмм.
Фиксированная скидка.
Иногда продавец заявляет о скидке в твердой сумме. Тогда покупатель может рассчитать величину скидки в процентах в обратном порядке:
ПС = ((БЦ – НЦ) / БЦ) * 100%, где
ПС – процент по скидке;
БЦ – базовая (старая) цена;
НЦ – новая цена.

Приведем расчет скидки.
Пример расчета процента скидки исходя из фиксированной величины
В договоре поставки между ООО «Аква» и ООО «Вега» предусмотрено, что стоимость доставки бутилированной воды за емкость 19л по договору составляет 160 рублей.
Если объем поставки за месяц превысит 190л, стоимость доставки будет ниже и составит 150 рублей.
Рассчитаем процент скидки, если условие о превышении объема поставок выполнено:
160 руб. – базовая цена,
150 руб. – новая цена.
Процент по скидке составит: ((160 – 150) / 160) * 100% = 6,25%.
То есть стоимость уменьшается на 6,25%, если по договору будет доставлено более 190 л воды в месяц.
Используя эти формулы, покупатель может самостоятельно рассчитать величину изменения цены, а также вычислить новую стоимость товара.
Это поможет определиться, стоит ли заключать сделку на предложенных условиях.

