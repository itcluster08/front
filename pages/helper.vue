Скидки могут быть предоставлены всем контрагентам без исключения или только тем, в ком продавец заинтересован.

Основание предоставления скидки.
В ст. 424 ГК РФ указано, что цена устанавливается соглашением сторон.
Изменение допускается по условиям заключенного договора, либо в случаях, оговоренных в законе (п. 1, 2 ст. 424 ГК РФ).
В рознице размер скидки обычно указывают в ценнике на товар, в отношении которого действует предложение.
Для привлечения покупателей размещают дополнительное объявление о действующей акции. В чеке итоговая сумма указывается с учетом всех скидок.
